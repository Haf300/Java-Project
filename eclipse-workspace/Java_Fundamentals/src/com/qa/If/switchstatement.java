package com.qa.If;

public class switchstatement {

	public static void main(String[] args) {
		    int day = 3;
		    switch(day) {
		    case 1:System.out.println("Day 1 Schedule");
		    case 2:System.out.println("Day 2 Schedule");
		    case 3:System.out.println("Day 3 Schedule");
		    case 4:System.out.println("Day 4 Schedule");
		    case 5:System.out.println("Day 5 Schedule");
		    case 6:System.out.println("Day 6 Schedule");
		    case 7:System.out.println("Day 7 Schedule");
		    default:System.out.println("No Schedule Found");
	
		    }

	}
	
}

